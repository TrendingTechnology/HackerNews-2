    class var semiBold: UIFont { return UIFont(name: "Poppins-SemiBold", size: 16)! }
    class var italic: UIFont { return UIFont(name: "Poppins-Italic", size: 16)! }
    class var bold: UIFont { return UIFont(name: "Poppins-Bold", size: 16)! }
    // swiftlint:enable force_unwrapping
}
